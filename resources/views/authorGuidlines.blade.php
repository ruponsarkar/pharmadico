                            Format</span></span></strong></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span
                    style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">The preferred
                        format of all manuscript is MS Word. Illustrations (figures) and images must be inserted in the
                        manuscript at the position they should appear when published.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Preparation of
                            Manuscript</span></span></strong></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span
                    style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Mode of
                        Presenting Paper is English. Each manuscript should be typed single-spaced on A4 (8.5&Prime;
                        &times; 11&Prime;) paper size with 1-inch margins. It should be arranged in the following order:
                        Title, Abstract, Keywords, Introduction, Materials and Methods, Results, Discussion, Conclusion,
                        Acknowledgement and References.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Title
                            Page</span></span></strong></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span
                    style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Title page
                        should contain title of the paper in bold face, title case (font size 14), names of the authors
                        in normal face, upper case (font size 12) followed by the address in normal face lower case. The
                        author to whom all correspondence be addressed should be denoted by an asterisk mark. The title
                        should be as short as possible and precisely indicate the nature of the work in the
                        communication. Names of the authors should appear as initials followed by surnames. At the
                        bottom left corner of the title page, please mention &ldquo;*Address For correspondence&rdquo;
                        and provide a functional e-mail address. Address of the corresponding author to whom all
                        correspondence may be sent should be given only if it is different from the address already
                        given under authors&rsquo; names.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Abstract</span></span></strong><br />
                <span style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Should start
                        on a new page after the title page and should be typed in single-space to distinguish it from
                        the Introduction. Abstracts should briefly reflect all aspects of the study, as most databases
                        list mainly abstracts. The manuscript should have an abstract 150- 250
                        words.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Keywords</span></span></strong><br />
                <span style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Provide four
                        to six appropriate key words after abstract</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Introduction</span></span></strong><br />
                <span style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">A short
                        introduction of the research problem followed by a brief review of literature and objective of
                        the research.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Materials and
                            Methods</span></span></strong></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span
                    style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Describe the
                        materials used in the experiment, year of experimentation, site etc. Describe the methods
                        implied for collection of data in short.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">Results and
                            Discussion</span></span></strong></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span
                    style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">This segment
                        should focus on the fulfillment of&nbsp; stated objectives as given in the introduction. It
                        should contain the findings presented in the form of tables, figures and
                        photographs.</span></span></span></span></p>

    <p style="text-align:justify"><span style="font-size:11pt"><span
                style="font-family:Calibri,sans-serif"><strong><span style="font-size:12.0pt"><span
                            style="font-family:&quot;Times New Roman&quot;,serif">References</span></span></strong><br />
                <span style="font-size:12.0pt"><span style="font-family:&quot;Times New Roman&quot;,serif">Should be
                        numbered consecutively in the order in which they are first mentioned in the text (not in
                        alphabetic order). Identify references in text, tables and legends by Arabic numerals in
                        superscript. References cited only in tables or figure legends should be numbered in accordance
                        with the sequence established by the first identification in the text of the particular table or
                        figure.</span></span></span></span></p>

